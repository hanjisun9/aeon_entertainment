    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        try{
            String U = username.getText();
            String p = new String(password.getPassword());
            
            if (U.isEmpty() || p.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Empty Password or Username", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Connection conn = convig.ConvigDB();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Database Connection Failed!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            //cek usn di database
            String sqlCekUsername = "SELECT * FROM users WHERE username_user = ?";
            java.sql.PreparedStatement pstCek = conn.prepareStatement(sqlCekUsername);
            pstCek.setString(1, U);
            ResultSet resUsername = pstCek.executeQuery();
            
            if (!resUsername.next()) {
                JOptionPane.showMessageDialog(this, "If you don't have an account yet! Please register first.", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                username.requestFocus();
                conn.close();
                return;
            }
            
            // cek password
            String sql = "SELECT * FROM users WHERE username_user = ? AND password_user = ?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, U);
            pst.setString(2, p);
            ResultSet res = pst.executeQuery();
            
            if (res.next()) {
                String role = res.getString("role");
                
                JOptionPane.showMessageDialog(this, "Successful Login!", "Success", JOptionPane.INFORMATION_MESSAGE);
                conn.close();
                Dashboard dashboardForm = new Dashboard();
                dashboardForm.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Wrong Password!", "Error", JOptionPane.ERROR_MESSAGE);
                password.setText("");
                password.requestFocus();
            }
            
            conn.close();
            
        } catch (SQLException exp) {
            JOptionPane.showMessageDialog(this, "Database Error: " + exp.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            exp.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_loginActionPerformed

